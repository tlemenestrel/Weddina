package weddina.week1;

import java.util.*;

public class Login {

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public static ArrayList <String> couplePasswords = new ArrayList <String>();
	public static ArrayList <String> coupleEmails = new ArrayList <String>();

	public static ArrayList <String> guestPasswords = new ArrayList <String>();
	public static ArrayList <String> guestEmails = new ArrayList <String>();

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public static boolean checkIfUserExists (String password, String email, ArrayList<String> listEmails) {

		if (listEmails.contains(email)) {

			return true;

		}

		else {

			return false;

		}

	}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public static void signUp (String password, String email,ArrayList<String> listPasswords, ArrayList<String> listEmails) {

		if (checkIfUserExists(password,email, listEmails) == false) {

			listPasswords.add(password);
			listEmails.add(email);

		}

	}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public static boolean signIn (String password, String email,ArrayList<String> listEmails, ArrayList<String> listPasswords) {

		if (checkIfUserExists(password,email,listEmails) == true) {

			if (listPasswords.contains(password)) {

				return true;

			}

			else {

				return false;

			}

		}

		else {

			return false;

		}

	}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}
