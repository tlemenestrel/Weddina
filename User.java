package weddina.week1;

import weddina.week1.Login;

import java.util.*;

public class User {

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    private String email;
    private String password;
    
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public User(String email, String password) {

        this.email = email;
        this.password = password;

    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public void changePasswordCouple (String newPassword) {

		if (Login.checkIfCoupleExists(email) == true) {

			if (Couple.couplePassword.equals(password)) {

				Couple.couplePassword = newPassword;

				setPassword(newPassword);

			}

		}

	}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public void changePasswordGuest (String newPassword, ArrayList<String> listPasswords, ArrayList<String> listEmails) {

		if (Login.checkIfGuestExists(email) == true) {

			if (listPasswords.contains(password)) {

				int index = listPasswords.indexOf(password);

				listPasswords.set(index, newPassword);
				setPassword(newPassword);

			}

		}

	}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public void changePasswordBusiness (String newPassword, ArrayList<String> listPasswords, ArrayList<String> listEmails) {

		if (Login.checkIfBusinessExists(email,listEmails) == true) {

			if (listPasswords.contains(password)) {

				int index = listPasswords.indexOf(password);

				listPasswords.set(index, newPassword);
				setPassword(newPassword);

			}

		}

	}
	
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	public String remindPasswordCouple () {

		if (Login.checkIfCoupleExists(email) == true) {

			return ("Your password is: " + getPassword());

		}

		else {

			return ("Sorry, you are not in our system");

		}

	}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public String remindPasswordGuest (ArrayList<String> listEmails) {

		if (Login.checkIfGuestExists(email) == true) {

			return ("Your password is: " + getPassword());

		}

		else {

			return ("Sorry, you are not in our system");

		}

	}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public String remindPasswordBusiness (ArrayList<String> listEmails) {

		if (Login.checkIfBusinessExists(email,listEmails) == true) {

			return ("Your password is: " + getPassword());

		}

		else {

			return ("Sorry, you are not in our system");

		}

	}
	
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public String getEmail() {

        return email;

    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public String getPassword() {

        return password;

    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    public void setPassword(String password) {

        this.password = password;

    }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}